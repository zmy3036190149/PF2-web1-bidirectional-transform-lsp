package com.example.demo.bean;

public class IntInfo {
	private int no;
	private String init;
	private String rece;
	private String type;
	private int state;

	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getInit() {
		return init;
	}
	public void setInit(String init) {
		this.init = init;
	}
	public String getRece() {
		return rece;
	}
	public void setRece(String rece) {
		this.rece = rece;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
}
